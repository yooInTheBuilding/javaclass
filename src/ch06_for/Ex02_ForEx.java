package ch06_for;

public class Ex02_ForEx {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
        for (int i = 1; i <= 5; i++){
            System.out.println(2*i);
        }
        for (int i = 1; i <= 5; i++){
            System.out.println(6-i);
        }
    }
}
